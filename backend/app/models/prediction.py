"""
SentinelAI — Prediction ORM Model

Stores the full ML inference result for every scan, including
SHAP values, plain-English explanation, and top contributing features.

Separated from URLScan/UPIScan so that:
  - We can store SHAP data independently of scan metadata
  - The prediction can be linked to a specific model version
  - We can re-run predictions against old scans when a new model ships

scan_type: "url" | "upi"
Exactly one of url_scan_id or upi_scan_id will be non-null.
"""

import uuid
from datetime import datetime
from typing import TYPE_CHECKING

from sqlalchemy import DateTime, ForeignKey, String, Text, func
from sqlalchemy.dialects.postgresql import JSON, UUID
from sqlalchemy.orm import Mapped, mapped_column, relationship

from app.db.base import Base

if TYPE_CHECKING:
    from app.models.url_scan import URLScan
    from app.models.upi_scan import UPIScan
    from app.models.model_version import ModelVersion


class Prediction(Base):
    __tablename__ = "predictions"

    id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True),
        primary_key=True,
        default=uuid.uuid4,
    )
    # "url" | "upi"
    scan_type: Mapped[str] = mapped_column(String(10), nullable=False, index=True)

    # Only one of these is non-null, based on scan_type
    url_scan_id: Mapped[uuid.UUID | None] = mapped_column(
        UUID(as_uuid=True),
        ForeignKey("url_scans.id", ondelete="CASCADE"),
        nullable=True,
        index=True,
    )
    upi_scan_id: Mapped[uuid.UUID | None] = mapped_column(
        UUID(as_uuid=True),
        ForeignKey("upi_scans.id", ondelete="CASCADE"),
        nullable=True,
        index=True,
    )
    model_version_id: Mapped[uuid.UUID | None] = mapped_column(
        UUID(as_uuid=True),
        ForeignKey("model_versions.id", ondelete="SET NULL"),
        nullable=True,
        index=True,
    )

    # ── SHAP Explainability Data ─────────────────────────────
    # Raw SHAP values dict: {"feature_name": shap_value, ...}
    shap_values_json: Mapped[dict | None] = mapped_column(JSON, nullable=True)
    # Human-readable explanation generated by ExplanationService
    explanation_text: Mapped[str | None] = mapped_column(Text, nullable=True)
    # Top N contributing features sorted by abs(shap_value)
    top_features_json: Mapped[dict | None] = mapped_column(JSON, nullable=True)

    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        nullable=False,
        server_default=func.now(),
    )

    # ── Relationships ────────────────────────────────────────
    url_scan: Mapped["URLScan | None"] = relationship(
        "URLScan",
        primaryjoin="Prediction.url_scan_id == URLScan.id",
        back_populates="prediction",
        foreign_keys=[url_scan_id],
    )
    upi_scan: Mapped["UPIScan | None"] = relationship(
        "UPIScan",
        primaryjoin="Prediction.upi_scan_id == UPIScan.id",
        back_populates="prediction",
        foreign_keys=[upi_scan_id],
    )
    model_version: Mapped["ModelVersion | None"] = relationship(
        "ModelVersion",
        back_populates="predictions",
    )

    def __repr__(self) -> str:
        return (
            f"<Prediction id={self.id} type={self.scan_type} "
            f"model_version_id={self.model_version_id}>"
        )
